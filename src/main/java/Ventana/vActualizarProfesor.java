    private void ActualizarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarBtnActionPerformed
        // TODO add your handling code here:
        String codigo = tCodigo.getText();
        String nombre = NombreInput.getText();
        String apellido = ApellidoInput.getText();
        String correo = CorreoInput.getText();
        String contrasenia = PasswordInput.getText();
        String genero = GeneroInput.getText();

        Profesor nuevoProfesor = new Profesor(codigo, nombre, apellido, correo, contrasenia, genero);
        if(count==false){
            int fila=num;
            
            vAdminProfesores.profesores[fila]= nuevoProfesor;
        }else{
        int fila = vAdminProfesores.fila;

        vAdminProfesores.profesores[fila] = nuevoProfesor;
        }

    }//GEN-LAST:event_ActualizarBtnActionPerformed

    public void meterDatos() {
        if(count==false){
            tCodigo.setText(profesores[num].codigo);
            NombreInput.setText(profesores[num].nombre);
            ApellidoInput.setText(profesores[num].apellido);
            CorreoInput.setText(profesores[num].correo);
            PasswordInput.setText(profesores[num].contrasenia);
            GeneroInput.setText(profesores[num].genero);
        }else{
        tCodigo.setText(vAdminProfesores.codigo);
        NombreInput.setText(vAdminProfesores.nombre);
        ApellidoInput.setText(vAdminProfesores.apellido);
        CorreoInput.setText(vAdminProfesores.correo);
        PasswordInput.setText(vAdminProfesores.contrasenia);
        GeneroInput.setText(vAdminProfesores.genero);
        }
    }

